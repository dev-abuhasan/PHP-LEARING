<?php
// PHP vs JavaScript

// PHP (Hypertext Preprocessor) is a server-side scripting language designed primarily for web development.
// It is executed on the server, and the result is sent to the client's web browser as plain HTML.
// PHP can interact with databases, handle forms, and perform server-side logic.

echo "Hello from PHP!";

// JavaScript is a client-side scripting language that runs in the client's web browser.
// It is used to create interactive and dynamic web pages by manipulating the HTML and CSS.
// JavaScript can respond to user actions, validate forms, and communicate with the server asynchronously.

echo "<script>
    document.write('Hello from JavaScript!');
      </script>";
